    public static class IndexGenerator {

        public static StringBuilder generate() {
            final var builder = new StringBuilder();
            builder.append("    /**\n");
            builder.append("        GENERATED\n");
            builder.append("    */\n");
            builder.append("    public static final int[][][][] RECIPE_INDEXES = {\n");

            for (int slots = 1; slots < 10; slots++) {
                appendSpaces(builder, 2 * 4);
                builder.append("{\n");

                for (int variants = slots - 1; variants >= 0; variants--) {
                    appendSpaces(builder, 3 * 4);
                    builder.append("{\n");

                    var input = new ArrayList<IntegerIndex>();
                    for (int j = 0; j < slots - variants; j++) {
                        input.add(new IntegerIndex(j, slots - j));
                    }
                    input.sort(Comparator.comparingInt(IntegerIndex::getSize).reversed());

                    var result = new ArrayList<List<IntegerIndex>>();
                    combination(result, input, Collections.emptyList(), 0, slots, slots, slots);

                    for (final List<IntegerIndex> sizeds : result) {
                        appendSpaces(builder, 4 * 4);
                        builder.append(
                                sizeds.stream()
                                        .map(Objects::toString)
                                        .collect(Collectors.joining(", ", "{ ", " }"))
                        );
                        builder.append(",\n");
                    }

                    appendSpaces(builder, 3 * 4);
                    builder.append("},\n");
                }

                appendSpaces(builder, 2 * 4);
                builder.append("},\n");
            }

            builder.append("    };");
            return builder;
        }

        private static void combination(List<List<IntegerIndex>> result, List<IntegerIndex> input, List<IntegerIndex> sub, int index, int prev, int left, int max) {
            if (left == 0) {
                return;
            }
            if (index >= max) return;
            if (index >= input.size()) return;

            for (int i = left; i > 0; i--) {
                var newSub = new ArrayList<>(sub);
                var count = Integer.min(i, prev);
                for (int j = 0; j < count; j++) {
                    final var elem = input.get(index);
                    newSub.add(new IntegerIndex(elem.i, elem.size / count));
                }

                if (left - i == 0 && newSub.size() == max) {
                    result.add(newSub);
                } else combination(result, input, newSub, index + 1, i, left - i, max);
            }
        }

        public static void appendSpaces(StringBuilder builder, int count) {
            builder.append(" ".repeat(Math.max(0, count)));
        }

        @Getter
        @AllArgsConstructor
        private static class IntegerIndex {

            private int i;
            private int size;

            @Override
            public String toString() {
                return String.valueOf(i);
            }
        }
    }

    public static class SizeGenerator {

        public static StringBuilder generate() {
            final var builder = new StringBuilder();
            builder.append("    /**\n");
            builder.append("        GENERATED\n");
            builder.append("    */\n");
            builder.append("    public static final int[][][][] INDEXES_SIZE = {\n");
            for (int i = 0; i < RECIPE_INDEXES.length; i++) {
                appendSpaces(builder, 2 * 4);
                builder.append("{\n");

                final var ingredientsCount = RECIPE_INDEXES[i];
                for (int i1 = 0; i1 < ingredientsCount.length; i1++) {
                    appendSpaces(builder, 3 * 4);
                    builder.append("{\n");

                    final var variants = ingredientsCount[i1];
                    for (int i2 = 0; i2 < variants.length; i2++) {
                        int[] counts = new int[i + 1];

                        final var variant = variants[i2];
                        for (final int j : variant) counts[j]++;

                        int[] out = new int[variant.length];
                        for (int j = 0; j < variant.length; j++) {
                            out[j] = counts[variant[j]];
                        }

                        appendSpaces(builder, 4 * 4);
                        builder.append(Arrays.stream(out)
                                .mapToObj(String::valueOf)
                                .collect(Collectors.joining(", ", "{ ", " },\n"))
                        );
                    }

                    appendSpaces(builder, 3 * 4);
                    builder.append("},\n");
                }

                appendSpaces(builder, 2 * 4);
                builder.append("},\n");
            }
            builder.append("    };");
            return builder;
        }

        public static void appendSpaces(StringBuilder builder, int count) {
            builder.append(" ".repeat(Math.max(0, count)));
        }

    }

